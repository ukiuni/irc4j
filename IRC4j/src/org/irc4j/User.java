/**
 * User in IRC server.
 * 
 * @author ukiuni
 */
public class User {
	private String nickName;
	private boolean owner;

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public boolean isOwner() {
		return owner;
	}

	public void setOwner(boolean owner) {
		this.owner = owner;
	}
}
